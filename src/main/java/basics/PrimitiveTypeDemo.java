package basics;

public class PrimitiveTypeDemo {

    //Javas 8 primitive data types

    //Har bara två möjliga värden, true eller false.
    boolean boolPrimitive;

    //8 bits, -128-127
    byte bytePrimitive;

    //16 bits, -32768-32767
    short shortPrimitive;

    //32 bits , -2^31 - 2^31-1
    int integerPrimitive;

    //-2^63-2^63-1
    long longPrimitive;

    //flyttal 32 bits
    float floatPrimitve;

    //flyttal 64 bits
    double doublePrimitive;

    //16 bits, representar ett tecken.
    char charPrimitive;

    //Wrapper-klasser för de primitiva datatyperna.

    Boolean boxedBooleam;

    Byte boxedByte;
    Short boxedShort;
    Integer boxedInteger;
    Long boxedLong;

    Float boxedFloat;
    Double boxedDouble;

    Character boxedChar;

}
